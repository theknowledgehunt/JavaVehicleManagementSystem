        String query = "DELETE FROM `vehicledetails` WHERE VehicleNo = ? ";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, vno);

            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Vehicle Data Removed");
                VehicleManagingForm.jTable1.setModel(new DefaultTableModel(null,new Object[] {"Vehicle No","Color","Model","Weight","Manufac.Year","OwnerId","Description"}));
                vh.fillVehicleJtable(VehicleManagingForm.jTable1, "");
            }

        } catch (SQLException ex) {
            Logger.getLogger(VehicleManagingForm.class.getName()).log(Level.SEVERE, null, ex);
        }
