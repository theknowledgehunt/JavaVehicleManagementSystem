    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed

        int rowIndex = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();

        String vno = jTextField_VNo.getText();
        String clr = jTextField_CLR.getText();
        String mdl = jTextField_Model.getText();
        String weight = jTextField_Weight.getText();
        String desc = jTextArea_Desc.getText();
        String myear = jTextField_myear.getText();
        String oid = jTextField_oid.getText();

        if(vno.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Add Vehicle Number");
        }
        
        else if(clr.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Add Vehicle color");
        }
        
        else if(oid.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Add Owner National Card Number");
        }
        
        else{
            
        PreparedStatement ps;
        String query = "UPDATE `vehicledetails` SET `VehicleNo` = ?, `color` = ?, `model` = ?, `weight` = ?, `manufacYear` = ?, `OwnerId` = ? , `descrip` = ? WHERE VehicleNo = ?;";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, vno);
            ps.setString(2, clr);
            ps.setString(3, mdl);
            ps.setString(4, weight);
            ps.setString(5, myear);
            ps.setString(6, oid);
            ps.setString(7, desc);
            ps.setString(8, vno);
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Vehicle Data Updated");
                UserManagingForm.jTable1.setModel(new DefaultTableModel(null,new Object[] {"Vehicle No","Color","Model","Weight","Manufac.Year","OwnerId", "Description"}));
                vh.fillVehicleJtable(VehicleManagingForm.jTable1, "");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(VehicleManagingForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    
    }//GEN-LAST:event_jButton_editActionPerformed

